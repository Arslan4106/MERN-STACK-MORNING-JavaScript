// Data Types

// => Data Primptive:
// 1. Number
let x = 89;
let y = 89.23;
console.log(x);
console.log(y);
console.log(typeof x, x);
console.log(typeof y, y);
// 2. String
let firstName = "Jonas";
let lastName = "Bob";
console.log(firstName, typeof firstName);
console.log(lastName, typeof lastName);
// 3. Boolean
let num1 = 34;
let num2 = 78;
let num = num1 > num2;
console.log(num, typeof num);
// 4. Undefined (empty Value)
let myFirstJob;
console.log(myFirstJob, typeof myFirstJob);
// 5. Null (empty Value)
let z = 0;
 z = 23 + 0;

